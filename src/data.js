// ── Semester order helper ────────────────────────────
export function getSemOrder(sub) {
  if (!sub) return 'other';
  if (/1.курс|лет\.ш|летн/i.test(sub)) return '1year';
  if (/3.сем|зима/i.test(sub))          return '3sem';
  if (/4.сем|весна/i.test(sub))         return '4sem';
  return 'other';
}

export function semLabel(code) {
  if (code === '3sem') return '— 3 семестр —';
  if (code === '4sem') return '— 4 семестр —';
  return '—';
}

